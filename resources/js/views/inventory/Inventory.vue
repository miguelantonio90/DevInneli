<template>
  <v-container>
    <v-row>
      <v-col
        class="py-0"
        cols="5"
      >
        <v-autocomplete
          chips
          clearable
          deletable-chips
          rounded
          solo
          :items="localArticles"
          item-text="name"
          return-object
          @input="selectArticle"
        />
      </v-col>
      <v-col cols="12">
        <v-card
          elevation="2"
          outlined
          shaped
          tile
        />
        <v-card-text>
          <v-data-table
            :headers="getTableColumns"
            :items="supplies_product"
          >
            <template v-slot:item.cost="{ item }">
              <v-edit-dialog
                :return-value.sync="item.cost"
                large
                persistent
                :cancel-text="$vuetify.lang.t('$vuetify.actions.cancel')"
                :save-text="$vuetify.lang.t('$vuetify.actions.edit')"
              >
                <div>{{ item.cost }}</div>
                <template v-slot:input>
                  <div class="mt-4 title">
                    {{ $vuetify.lang.t('$vuetify.actions.edit') }}
                  </div>
                </template>
                <template v-slot:input>
                  <v-text-field-money
                    v-model="item.cost"
                    :label="$vuetify.lang.t('$vuetify.actions.edit')"
                    required
                    :properties="{
                      clearable: true
                    }"
                    :options="{
                      length: 15,
                      precision: 2,
                      empty: 0.00,
                    }"
                  />
                </template>
              </v-edit-dialog>
            </template>
            <template v-slot:item.cant="{ item }">
              <v-edit-dialog
                :return-value.sync="item.cant"
                large
                persistent
                :cancel-text="$vuetify.lang.t('$vuetify.actions.cancel')"
                :save-text="$vuetify.lang.t('$vuetify.actions.edit')"
              >
                <div>{{ item.cant }}</div>
                <template v-slot:input>
                  <div class="mt-4 title">
                    {{ $vuetify.lang.t('$vuetify.actions.edit') }}
                  </div>
                </template>
                <template v-slot:input>
                  <v-text-field
                    v-model="item.cant"
                    label="Edit"
                    single-line
                    counter
                    autofocus
                  />
                </template>
              </v-edit-dialog>
            </template>
            <template v-slot:item.actions="{ item }">
              <v-icon
                small
                @click="deleteItem(item)"
              >
                mdi-delete
              </v-icon>
            </template>
          </v-data-table>
        </v-card-text>
      </v-col>
      <v-col
        v-show="supplies_product.length > 0"
        cols="6"
      >
        <detail-supplier
          :shop-supply="shopSupply"
          :no-facture-supply="noFactureSupply"
          :supplier-supply="supplier_supply"
          :tax-supply="tax_supply"
          @updateSupplyData="updateSupplyData"
        />
      </v-col>
    </v-row>
  </v-container>
</template>
<script>
import { mapActions, mapState } from 'vuex'
import DetailSupplier from './DatailSupplier'

export default {
  name: 'Inventory',
  components: { DetailSupplier },
  data () {
    return {
      supplies_product: [],
      localArticles: [],
      supplier_supply: null,
      tax_supply: null,
      noFactureSupply: '',
      shopSupply: null
    }
  },
  computed: {
    ...mapState('article', [
      'showNewModal',
      'showEditModal',
      'showShowModal',
      'articles',
      'isTableLoading'
    ]),
    getTableColumns () {
      return [
        {
          text: this.$vuetify.lang.t('$vuetify.articles.ref'),
          value: 'ref',
          select_filter: true
        },
        {
          text: this.$vuetify.lang.t('$vuetify.firstName'),
          value: 'name',
          select_filter: true
        },
        {
          text: this.$vuetify.lang.t('$vuetify.articles.inventory'),
          value: 'inventory',
          select_filter: true
        },
        {
          text: this.$vuetify.lang.t('$vuetify.articles.price'),
          value: 'price',
          select_filter: true
        },
        {
          text: this.$vuetify.lang.t('$vuetify.articles.cost'),
          value: 'cost',
          select_filter: true
        },
        {
          text: this.$vuetify.lang.t('$vuetify.variants.cant'),
          value: 'cant',
          select_filter: true
        },
        {
          text: this.$vuetify.lang.t('$vuetify.actions.actions'),
          value: 'actions',
          sortable: false
        }
      ]
    }
  },
  watch: {
  },
  created () {
    this.getArticles().then(() => {
      this.articles.forEach((value) => {
        if (!value.parent_id) {
          let inventory = 0
          if (value.variant_values.length > 0) {
            value.variant_values.forEach((v) => {
              if (v.articles_shops.length > 0) {
                v.articles_shops.forEach((k) => {
                  inventory += k.under_inventory ? parseFloat(k.under_inventory) : 0
                })
              }
              this.localArticles.push({
                ref: value.ref,
                name: value.name + '(' + v.name + ')',
                price: v.price ? v.price : 0,
                cost: v.cost ? v.cost : 0,
                inventory: inventory || 0,
                tax: 0,
                cant: 1,
                supplier: '',
                noFacture: '',
                totalCost: v.cost,
                totalPrice: v.price,
                article_id: v.id
              })
            })
          } else {
            if (value.articles_shops.length > 0) {
              value.articles_shops.forEach((k) => {
                inventory += k.under_inventory ? parseFloat(k.under_inventory) : 0
              })
            }
            this.localArticles.push({
              ref: value.ref,
              name: value.name,
              price: value.price ? value.price : 0,
              cost: value.cost ? value.cost : 0,
              cant: 1,
              tax: 0,
              inventory: inventory || 0,
              noFacture: '',
              totalCost: value.cost,
              supplier: '',
              totalPrice: value.price,
              article_id: value.id
            })
          }
        }
      })
    })
  },
  methods: {
    ...mapActions('article', ['getArticles']),
    selectArticle (item) {
      this.supplies_product.push(item)
      this.supply = item
    },
    updateSupplyData () {
      console.log('updateSupplyData')
    },
    deleteItem (item) {
      this.supplies_product.splice(this.supplies_product.indexOf(item), 1)
    }
  }
}
</script>

